const hubToken = token('second'); // 'second-s'
const secondTo = new Map();
const toSecond = new Map();

for (const [k, f] of Object.entries(conversionFactors)) {
  const parts = k.split('-to-'); // e.g., ['second-s', 'minute-min']
  if (parts.length !== 2) continue;
  const [A, B] = parts;
  if (A === hubToken) secondTo.set(B, f);
  if (B === hubToken) toSecond.set(A, f);
}
secondTo.set(hubToken, 1);
toSecond.set(hubToken, 1);

